package com.itheima.po;
public class instructor {
private String ins_id;
private String ins_password;
private String ins_campus;
private String ins_name;
private String ins_phone;
public String getIns_id() {
	return ins_id;
}
public void setIns_id(String ins_id) {
	this.ins_id = ins_id;
}
public String getIns_password() {
	return ins_password;
}
public void setIns_password(String ins_password) {
	this.ins_password = ins_password;
}
public String getIns_campus() {
	return ins_campus;
}
public void setIns_campus(String ins_campus) {
	this.ins_campus = ins_campus;
}
public String getIns_name() {
	return ins_name;
}
public void setIns_name(String ins_name) {
	this.ins_name = ins_name;
}
public String getIns_phone() {
	return ins_phone;
}
public void setIns_phone(String ins_phone) {
	this.ins_phone = ins_phone;
}
}
